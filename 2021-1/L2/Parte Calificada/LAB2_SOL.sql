SELECT S.NOMBRE_SECTOR AS SECTOR, TE.NOMBRE_TIPO AS TIPO_EVENTO, E.NOMBRE AS EVENTO, P.NOMBRE AS PROVINCIA
FROM GE_SECTOR S, GE_TIPO_EVENTO TE, GE_EVENTO E, GE_DISTRITO D, GE_PROVINCIA P 
WHERE S.ID_SECTOR = E.ID_SECTOR
AND E.ID_TIPO_EVENTO = TE.ID_TIPO_EVENTO
AND E.UBIGEO = D.ID_DISTRITO
AND D.ID_PROVINCIA = P.ID_PROVINCIA
AND E.VIRTUAL = 0
AND E.ESTADO = 'F'
ORDER BY P.NOMBRE ASC; 

